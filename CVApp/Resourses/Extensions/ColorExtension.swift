import SwiftUI

extension Color {
    static var mainPurpleColor: Color {
        return Color(UIColor.systemIndigo)
    }
    
    static var lightGrayColor: Color {
        return Color(UIColor.lightGray)
    }
    
    static var systemGray2Color: Color {
        return Color(UIColor.systemGray2)
    }
    
    static var labelColor: Color {
        return Color(UIColor.label)
    }
    
    static var secondaryLabelColor: Color {
        return Color(UIColor.secondaryLabel)
    }
    
    static var systemBackgroundColor: Color {
        return Color(UIColor.systemBackground)
    }
    
    static var secondarySystemBackgroundColor: Color {
        return Color(UIColor.secondarySystemBackground)
    }
    
    static var systemGreenColor: Color {
        return Color(UIColor.systemGreen)
    }
    
    static var systemBlueColor: Color {
        return Color(UIColor.systemBlue)
    }
    
    static var systemRedColor: Color {
        return Color(UIColor.systemRed)
    }
    
    static var systemYellowColor: Color {
        return Color(UIColor.systemYellow)
    }
    
}
